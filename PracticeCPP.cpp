#include <bits/stdc++.h>
using namespace std;

void printvector(vector<int> v)
{
    cout<<"Size : "<<v.size()<<endl;
    for (int i = 0; i < v.size(); i++)
    {
        cout<<v[i]<<" ";
    }
    cout<<endl;
    
}

void printvecpai(vector<pair<int,int>> v)
{
    cout<<"Size : "<<v.size()<<endl;
    for (int i = 0; i < v.size(); i++)
    {
        cout<<v[i].first<<" "<<v[i].second<<endl;
    }
    cout<<endl;
    
}

